// Түсіндірме: { Prisma } модулін осы файлда қолдану үшін жүктейді.
const { Prisma } = require("@prisma/client");
// Түсіндірме: ApiError модулін осы файлда қолдану үшін жүктейді.
const ApiError = require("../utils/ApiError");

// Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
const statusNames = {
  // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
  400: "Bad Request",
  // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
  401: "Unauthorized",
  // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
  403: "Forbidden",
  // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
  404: "Not Found",
  // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
  409: "Conflict",
  // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
  422: "Unprocessable Entity",
  // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
  429: "Too Many Requests",
  // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
  500: "Internal Server Error"
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
};

// Түсіндірме: Қайта қолданылатын функцияны анықтайды.
function notFound(_req, _res, next) {
  // Түсіндірме: Басқаруды келесі middleware немесе error handler-ге береді.
  next(new ApiError(404, "Route not found"));
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
}

// Түсіндірме: Қайта қолданылатын функцияны анықтайды.
function errorHandler(err, _req, res, _next) {
  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  let statusCode = err.statusCode || 500;
  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  let message = err.message || "An unexpected error occurred";

  // Түсіндірме: Шартты тексеріп, орындалатын тармақты бастайды.
  if (err instanceof Prisma.PrismaClientKnownRequestError && err.code === "P2002") {
    // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
    statusCode = 422;
    // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
    message = "Unique constraint violation";
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  }

  // Түсіндірме: Шартты тексеріп, орындалатын тармақты бастайды.
  if (err.isJoi) {
    // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
    statusCode = 400;
    // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
    message = err.details.map((detail) => detail.message).join("; ");
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  }

  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  const payload = {
    // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
    statusCode,
    // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
    error: statusNames[statusCode] || "Error",
    // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
    message
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  };

  // Түсіндірме: Шартты тексеріп, орындалатын тармақты бастайды.
  if (err.details) {
    // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
    payload.details = err.details;
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  }

  // Түсіндірме: HTTP жауабын клиентке қайтарады.
  res.status(statusCode).json(payload);
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
}

// Түсіндірме: Бұл файлдан сыртқа берілетін функциялар мен мәндерді көрсетеді.
module.exports = { notFound, errorHandler };
