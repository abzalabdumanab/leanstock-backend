// Түсіндірме: Redis модулін осы файлда қолдану үшін жүктейді.
const Redis = require("ioredis");
// Түсіндірме: Redlock модулін осы файлда қолдану үшін жүктейді.
const Redlock = require("redlock").default || require("redlock");
// Түсіндірме: env модулін осы файлда қолдану үшін жүктейді.
const env = require("./env");

// Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
const redis = new Redis(env.redisUrl, {
  // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
  maxRetriesPerRequest: 2,
  // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
  enableOfflineQueue: env.nodeEnv !== "production"
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
});

// Түсіндірме: Код блогын немесе объект құрылымын бастайды.
redis.on("error", (error) => {
  // Түсіндірме: Шартты тексеріп, орындалатын тармақты бастайды.
  if (env.nodeEnv !== "test") {
    // Түсіндірме: Консольге диагностикалық хабарлама шығарады.
    console.error("Redis error:", error.message);
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  }
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
});

// Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
const redlock = new Redlock([redis], {
  // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
  retryCount: 8,
  // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
  retryDelay: 100,
  // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
  retryJitter: 50
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
});

// Түсіндірме: Асинхронды операция орындайтын функцияны анықтайды.
async function disconnectRedis() {
  // Түсіндірме: Асинхронды операцияның аяқталуын күтеді.
  await redis.quit();
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
}

// Түсіндірме: Бұл файлдан сыртқа берілетін функциялар мен мәндерді көрсетеді.
module.exports = { redis, redlock, disconnectRedis };
