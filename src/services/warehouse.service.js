// Түсіндірме: { prisma } модулін осы файлда қолдану үшін жүктейді.
const { prisma } = require("../config/database");
// Түсіндірме: ApiError модулін осы файлда қолдану үшін жүктейді.
const ApiError = require("../utils/ApiError");

// Түсіндірме: Асинхронды операция орындайтын функцияны анықтайды.
async function listWarehouses(tenantId) {
  // Түсіндірме: Функциядан нәтижені қайтарады.
  return prisma.warehouse.findMany({
    // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
    where: { tenantId, isActive: true },
    // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
    orderBy: { createdAt: "desc" }
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  });
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
}

// Түсіндірме: Асинхронды операция орындайтын функцияны анықтайды.
async function createWarehouse(tenantId, body) {
  // Түсіндірме: Функциядан нәтижені қайтарады.
  return prisma.warehouse.create({
    // Түсіндірме: Код блогын немесе объект құрылымын бастайды.
    data: {
      // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
      tenantId,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      name: body.name,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      address: body.address
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    }
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  });
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
}

// Түсіндірме: Асинхронды операция орындайтын функцияны анықтайды.
async function assertWarehouse(tx, tenantId, id, label) {
  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  const warehouse = await tx.warehouse.findFirst({ where: { id, tenantId, isActive: true } });
  // Түсіндірме: Шартты тексеріп, орындалатын тармақты бастайды.
  if (!warehouse) {
    // Түсіндірме: Қате жағдайында exception тастайды.
    throw new ApiError(404, `${label} warehouse not found`);
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  }
  // Түсіндірме: Функциядан нәтижені қайтарады.
  return warehouse;
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
}

// Түсіндірме: Бұл файлдан сыртқа берілетін функциялар мен мәндерді көрсетеді.
module.exports = { listWarehouses, createWarehouse, assertWarehouse };
