// Түсіндірме: { prisma } модулін осы файлда қолдану үшін жүктейді.
const { prisma } = require("../config/database");
// Түсіндірме: { redlock } модулін осы файлда қолдану үшін жүктейді.
const { redlock } = require("../config/redis");
// Түсіндірме: ApiError модулін осы файлда қолдану үшін жүктейді.
const ApiError = require("../utils/ApiError");
// Түсіндірме: { assertWarehouse } модулін осы файлда қолдану үшін жүктейді.
const { assertWarehouse } = require("./warehouse.service");
// Түсіндірме: { pagePagination, buildPageMeta } модулін осы файлда қолдану үшін жүктейді.
const { pagePagination, buildPageMeta } = require("../utils/pagination");

// Түсіндірме: Қайта қолданылатын функцияны анықтайды.
function serializeTransfer(transfer) {
  // Түсіндірме: Функциядан нәтижені қайтарады.
  return {
    // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
    ...transfer,
    // Түсіндірме: Код блогын немесе объект құрылымын бастайды.
    items: transfer.items?.map((item) => ({
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      variantId: item.variantId,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      quantity: item.quantity
    // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
    }))
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  };
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
}

// Түсіндірме: Қайта қолданылатын функцияны анықтайды.
function normalizeItems(items) {
  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  const totals = new Map();
  // Түсіндірме: Коллекция элементтерін бір-бірлеп өңдейтін цикл бастайды.
  for (const item of items) {
    // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
    totals.set(item.variantId, (totals.get(item.variantId) || 0) + item.quantity);
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  }
  // Түсіндірме: Функциядан нәтижені қайтарады.
  return Array.from(totals, ([variantId, quantity]) => ({ variantId, quantity }));
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
}

// Түсіндірме: Асинхронды операция орындайтын функцияны анықтайды.
async function listTransfers(tenantId, query) {
  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  const { page, limit, skip } = pagePagination(query);
  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  const where = {
    // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
    tenantId,
    // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
    ...(query.status ? { status: query.status } : {}),
    // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
    ...(query.sourceId ? { sourceId: query.sourceId } : {}),
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    ...(query.destinationId ? { destinationId: query.destinationId } : {})
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  };
  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  const [total, transfers] = await prisma.$transaction([
    // Түсіндірме: Prisma арқылы дерекқормен жұмыс істейді.
    prisma.transfer.count({ where }),
    // Түсіндірме: Код блогын немесе объект құрылымын бастайды.
    prisma.transfer.findMany({
      // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
      where,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      include: { items: true },
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      orderBy: { createdAt: "desc" },
      // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
      skip,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      take: limit
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    })
  // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
  ]);
  // Түсіндірме: Функциядан нәтижені қайтарады.
  return { data: transfers.map(serializeTransfer), pagination: buildPageMeta(total, page, limit) };
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
}

// Түсіндірме: Асинхронды операция орындайтын функцияны анықтайды.
async function createTransfer(tenantId, userId, body) {
  // Түсіндірме: Шартты тексеріп, орындалатын тармақты бастайды.
  if (body.sourceId === body.destinationId) {
    // Түсіндірме: Қате жағдайында exception тастайды.
    throw new ApiError(422, "Source and destination cannot be the same");
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  }

  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  const items = normalizeItems(body.items);
  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  const resources = items
    // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
    .map((item) => `locks:tenant:${tenantId}:stock:${body.sourceId}:${item.variantId}`)
    // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
    .sort();

  // Түсіндірме: Функциядан нәтижені қайтарады.
  return redlock.using(resources, 8000, async () => prisma.$transaction(async (tx) => {
    // Түсіндірме: Асинхронды операцияның аяқталуын күтеді.
    await assertWarehouse(tx, tenantId, body.sourceId, "Source");
    // Түсіндірме: Асинхронды операцияның аяқталуын күтеді.
    await assertWarehouse(tx, tenantId, body.destinationId, "Destination");

    // Түсіндірме: Коллекция элементтерін бір-бірлеп өңдейтін цикл бастайды.
    for (const item of items) {
      // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
      const variant = await tx.productVariant.findFirst({ where: { id: item.variantId, tenantId } });
      // Түсіндірме: Шартты тексеріп, орындалатын тармақты бастайды.
      if (!variant) {
        // Түсіндірме: Қате жағдайында exception тастайды.
        throw new ApiError(404, `Variant with id ${item.variantId} not found`);
      // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
      }

      // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
      const stock = await tx.stockEntry.findUnique({
        // Түсіндірме: Код блогын немесе объект құрылымын бастайды.
        where: {
          // Түсіндірме: Код блогын немесе объект құрылымын бастайды.
          tenantId_variantId_warehouseId: {
            // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
            tenantId,
            // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
            variantId: item.variantId,
            // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
            warehouseId: body.sourceId
          // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
          }
        // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
        }
      // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
      });

      // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
      const available = stock?.quantity || 0;
      // Түсіндірме: Шартты тексеріп, орындалатын тармақты бастайды.
      if (available < item.quantity) {
        // Түсіндірме: Қате жағдайында exception тастайды.
        throw new ApiError(409, `Insufficient stock at source warehouse: requested ${item.quantity}, available ${available}`);
      // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
      }

      // Түсіндірме: Асинхронды операцияның аяқталуын күтеді.
      await tx.stockEntry.update({
        // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
        where: { id: stock.id },
        // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
        data: { quantity: { decrement: item.quantity } }
      // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
      });
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    }

    // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
    const transfer = await tx.transfer.create({
      // Түсіндірме: Код блогын немесе объект құрылымын бастайды.
      data: {
        // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
        tenantId,
        // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
        sourceId: body.sourceId,
        // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
        destinationId: body.destinationId,
        // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
        note: body.note,
        // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
        createdById: userId,
        // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
        status: "IN_TRANSIT",
        // Түсіндірме: Код блогын немесе объект құрылымын бастайды.
        items: {
          // Түсіндірме: Код блогын немесе объект құрылымын бастайды.
          create: items.map((item) => ({
            // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
            tenantId,
            // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
            variantId: item.variantId,
            // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
            quantity: item.quantity
          // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
          }))
        // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
        }
      // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
      },
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      include: { items: true }
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    });

    // Түсіндірме: Функциядан нәтижені қайтарады.
    return serializeTransfer(transfer);
  // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
  }));
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
}

// Түсіндірме: Асинхронды операция орындайтын функцияны анықтайды.
async function confirmTransfer(tenantId, id) {
  // Түсіндірме: Функциядан нәтижені қайтарады.
  return prisma.$transaction(async (tx) => {
    // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
    const transfer = await tx.transfer.findFirst({
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      where: { id, tenantId },
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      include: { items: true }
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    });
    // Түсіндірме: Шартты тексеріп, орындалатын тармақты бастайды.
    if (!transfer) {
      // Түсіндірме: Қате жағдайында exception тастайды.
      throw new ApiError(404, "Transfer not found");
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    }
    // Түсіндірме: Шартты тексеріп, орындалатын тармақты бастайды.
    if (transfer.status !== "IN_TRANSIT") {
      // Түсіндірме: Қате жағдайында exception тастайды.
      throw new ApiError(409, "Transfer is not in IN_TRANSIT status");
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    }

    // Түсіндірме: Коллекция элементтерін бір-бірлеп өңдейтін цикл бастайды.
    for (const item of transfer.items) {
      // Түсіндірме: Асинхронды операцияның аяқталуын күтеді.
      await tx.stockEntry.upsert({
        // Түсіндірме: Код блогын немесе объект құрылымын бастайды.
        where: {
          // Түсіндірме: Код блогын немесе объект құрылымын бастайды.
          tenantId_variantId_warehouseId: {
            // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
            tenantId,
            // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
            variantId: item.variantId,
            // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
            warehouseId: transfer.destinationId
          // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
          }
        // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
        },
        // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
        update: { quantity: { increment: item.quantity } },
        // Түсіндірме: Код блогын немесе объект құрылымын бастайды.
        create: {
          // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
          tenantId,
          // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
          variantId: item.variantId,
          // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
          warehouseId: transfer.destinationId,
          // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
          quantity: item.quantity
        // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
        }
      // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
      });
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    }

    // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
    const updated = await tx.transfer.update({
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      where: { id },
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      data: { status: "COMPLETED", completedAt: new Date() },
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      include: { items: true }
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    });
    // Түсіндірме: Функциядан нәтижені қайтарады.
    return serializeTransfer(updated);
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  });
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
}

// Түсіндірме: Асинхронды операция орындайтын функцияны анықтайды.
async function rejectTransfer(tenantId, id) {
  // Түсіндірме: Функциядан нәтижені қайтарады.
  return prisma.$transaction(async (tx) => {
    // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
    const transfer = await tx.transfer.findFirst({
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      where: { id, tenantId },
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      include: { items: true }
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    });
    // Түсіндірме: Шартты тексеріп, орындалатын тармақты бастайды.
    if (!transfer) {
      // Түсіндірме: Қате жағдайында exception тастайды.
      throw new ApiError(404, "Transfer not found");
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    }
    // Түсіндірме: Шартты тексеріп, орындалатын тармақты бастайды.
    if (transfer.status === "COMPLETED") {
      // Түсіндірме: Қате жағдайында exception тастайды.
      throw new ApiError(409, "Transfer already completed");
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    }
    // Түсіндірме: Шартты тексеріп, орындалатын тармақты бастайды.
    if (transfer.status !== "IN_TRANSIT") {
      // Түсіндірме: Қате жағдайында exception тастайды.
      throw new ApiError(409, "Transfer is not in IN_TRANSIT status");
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    }

    // Түсіндірме: Коллекция элементтерін бір-бірлеп өңдейтін цикл бастайды.
    for (const item of transfer.items) {
      // Түсіндірме: Асинхронды операцияның аяқталуын күтеді.
      await tx.stockEntry.update({
        // Түсіндірме: Код блогын немесе объект құрылымын бастайды.
        where: {
          // Түсіндірме: Код блогын немесе объект құрылымын бастайды.
          tenantId_variantId_warehouseId: {
            // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
            tenantId,
            // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
            variantId: item.variantId,
            // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
            warehouseId: transfer.sourceId
          // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
          }
        // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
        },
        // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
        data: { quantity: { increment: item.quantity } }
      // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
      });
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    }

    // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
    const updated = await tx.transfer.update({
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      where: { id },
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      data: { status: "REJECTED", completedAt: new Date() },
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      include: { items: true }
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    });
    // Түсіндірме: Функциядан нәтижені қайтарады.
    return serializeTransfer(updated);
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  });
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
}

