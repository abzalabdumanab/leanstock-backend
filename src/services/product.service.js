// Түсіндірме: { prisma } модулін осы файлда қолдану үшін жүктейді.
const { prisma } = require("../config/database");
// Түсіндірме: ApiError модулін осы файлда қолдану үшін жүктейді.
const ApiError = require("../utils/ApiError");
// Түсіндірме: { cursorPagination, buildCursorPage } модулін осы файлда қолдану үшін жүктейді.
const { cursorPagination, buildCursorPage } = require("../utils/pagination");

// Түсіндірме: Қайта қолданылатын функцияны анықтайды.
function serializeProduct(product) {
  // Түсіндірме: Функциядан нәтижені қайтарады.
  return {
    // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
    ...product,
    // Түсіндірме: Код блогын немесе объект құрылымын бастайды.
    variants: product.variants?.map((variant) => ({
      // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
      ...variant,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      costPrice: variant.costPrice.toString(),
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      retailPrice: variant.retailPrice.toString(),
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      currentPrice: variant.currentPrice.toString(),
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      liquidationPrice: variant.liquidationPrice.toString()
    // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
    }))
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  };
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
}

// Түсіндірме: Асинхронды операция орындайтын функцияны анықтайды.
async function listProducts(tenantId, query) {
  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  const { limit, cursor } = cursorPagination(query);
  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  const where = {
    // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
    tenantId,
    // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
    isActive: true,
    // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
    ...(query.category ? { category: query.category } : {}),
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    ...(query.search ? { name: { contains: query.search, mode: "insensitive" } } : {})
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  };

  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  const items = await prisma.product.findMany({
    // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
    where,
    // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
    include: { variants: true },
    // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
    orderBy: { createdAt: "desc" },
    // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
    take: limit + 1,
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    ...(cursor ? { cursor: { id: cursor }, skip: 1 } : {})
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  });

  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  const page = buildCursorPage(items.map(serializeProduct), limit);
  // Түсіндірме: Функциядан нәтижені қайтарады.
  return page;
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
}

// Түсіндірме: Асинхронды операция орындайтын функцияны анықтайды.
async function createProduct(tenantId, body) {
  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  const skus = body.variants.map((variant) => variant.sku);
  // Түсіндірме: Шартты тексеріп, орындалатын тармақты бастайды.
  if (new Set(skus).size !== skus.length) {
    // Түсіндірме: Қате жағдайында exception тастайды.
    throw new ApiError(400, "Variant SKUs must be unique inside the request");
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  }

  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  const product = await prisma.product.create({
    // Түсіндірме: Код блогын немесе объект құрылымын бастайды.
    data: {
      // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
      tenantId,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      name: body.name,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      description: body.description,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      category: body.category,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      baseUnit: body.baseUnit,
      // Түсіндірме: Код блогын немесе объект құрылымын бастайды.
      variants: {
        // Түсіндірме: Код блогын немесе объект құрылымын бастайды.
        create: body.variants.map((variant) => ({
          // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
          tenantId,
          // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
          ...variant,
          // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
          currentPrice: variant.retailPrice
        // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
        }))
      // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
      }
    // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
    },
    // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
    include: { variants: true }
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  });

  // Түсіндірме: Функциядан нәтижені қайтарады.
  return serializeProduct(product);
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
}

// Түсіндірме: Асинхронды операция орындайтын функцияны анықтайды.
async function getProduct(tenantId, id) {
  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  const product = await prisma.product.findFirst({
    // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
    where: { id, tenantId, isActive: true },
    // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
    include: { variants: true }
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  });
  // Түсіндірме: Шартты тексеріп, орындалатын тармақты бастайды.
  if (!product) {
    // Түсіндірме: Қате жағдайында exception тастайды.
    throw new ApiError(404, `Product with id ${id} not found`);
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  }
  // Түсіндірме: Функциядан нәтижені қайтарады.
  return serializeProduct(product);
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
}

// Түсіндірме: Бұл файлдан сыртқа берілетін функциялар мен мәндерді көрсетеді.
module.exports = { listProducts, createProduct, getProduct };
