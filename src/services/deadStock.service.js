// Түсіндірме: Қайта қолданылатын функцияны анықтайды.
function daysBetween(a, b) {
  // Түсіндірме: Функциядан нәтижені қайтарады.
  return Math.floor((a.getTime() - b.getTime()) / 86400000);
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
}

// Түсіндірме: Қайта қолданылатын функцияны анықтайды.
function calculateDecayedPrice({
  // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
  currentPrice,
  // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
  liquidationPrice,
  // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
  lastSaleDate,
  // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
  lastDecayAppliedAt,
  // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
  now = new Date(),
  // Түсіндірме: Тесттерден кейін орындалатын тазалау қадамын анықтайды.
  afterDays,
  // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
  decayPercent,
  // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
  decayIntervalDays
// Түсіндірме: Код блогын немесе объект құрылымын бастайды.
}) {
  // Түсіндірме: Шартты тексеріп, орындалатын тармақты бастайды.
  if (!lastSaleDate || daysBetween(now, lastSaleDate) < afterDays) {
    // Түсіндірме: Функциядан нәтижені қайтарады.
    return Number(currentPrice);
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  }

  // Түсіндірме: Шартты тексеріп, орындалатын тармақты бастайды.
  if (lastDecayAppliedAt && daysBetween(now, lastDecayAppliedAt) < decayIntervalDays) {
    // Түсіндірме: Функциядан нәтижені қайтарады.
    return Number(currentPrice);
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  }

  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  const decayed = Number(currentPrice) * (1 - Number(decayPercent) / 100);
  // Түсіндірме: Функциядан нәтижені қайтарады.
  return Math.max(Number(liquidationPrice), Number(decayed.toFixed(2)));
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
}

// Түсіндірме: Асинхронды операция орындайтын функцияны анықтайды.
async function applyDeadStockDecay(prisma, rules) {
  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  const now = new Date();
  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  const cutoff = new Date(now.getTime() - rules.afterDays * 86400000);

  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  const candidates = await prisma.productVariant.findMany({
    // Түсіндірме: Код блогын немесе объект құрылымын бастайды.
    where: {
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      lastSaleDate: { lt: cutoff }
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    }
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  });

  // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
  const updates = [];
  // Түсіндірме: Коллекция элементтерін бір-бірлеп өңдейтін цикл бастайды.
  for (const variant of candidates) {
    // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
    const nextPrice = calculateDecayedPrice({
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      currentPrice: variant.currentPrice,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      liquidationPrice: variant.liquidationPrice,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      lastSaleDate: variant.lastSaleDate,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      lastDecayAppliedAt: variant.lastDecayAppliedAt,
      // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
      now,
      // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
      ...rules
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    });
    // Түсіндірме: Шартты тексеріп, орындалатын тармақты бастайды.
    if (nextPrice < Number(variant.currentPrice) && Number(variant.currentPrice) > Number(variant.liquidationPrice)) {
      // Түсіндірме: Код блогын немесе объект құрылымын бастайды.
      updates.push(prisma.productVariant.update({
        // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
        where: { id: variant.id },
        // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
        data: { currentPrice: nextPrice, lastDecayAppliedAt: now }
      // Түсіндірме: Осы жол ағымдағы логиканың бір қадамын орындайды.
      }));
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    }
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  }

  // Түсіндірме: Шартты тексеріп, орындалатын тармақты бастайды.
  if (updates.length) {
    // Түсіндірме: Асинхронды операцияның аяқталуын күтеді.
    await prisma.$transaction(updates);
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  }
  // Түсіндірме: Функциядан нәтижені қайтарады.
  return { scanned: candidates.length, updated: updates.length };
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
}

// Түсіндірме: Бұл файлдан сыртқа берілетін функциялар мен мәндерді көрсетеді.
module.exports = { calculateDecayedPrice, applyDeadStockDecay };
