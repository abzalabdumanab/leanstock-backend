// Түсіндірме: { calculateDecayedPrice } модулін осы файлда қолдану үшін жүктейді.
const { calculateDecayedPrice } = require("../../src/services/deadStock.service");

// Түсіндірме: Jest тесттерінің тобын сипаттайды.
describe("dead stock price decay", () => {
  // Түсіндірме: Нақты бір тест сценарийін анықтайды.
  test("applies configurable percentage after the threshold", () => {
    // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
    const price = calculateDecayedPrice({
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      currentPrice: 1000,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      liquidationPrice: 500,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      lastSaleDate: new Date("2026-03-01T00:00:00Z"),
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      now: new Date("2026-05-01T00:00:00Z"),
      // Түсіндірме: Тесттерден кейін орындалатын тазалау қадамын анықтайды.
      afterDays: 30,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      decayPercent: 12.5,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      decayIntervalDays: 3
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    });

    // Түсіндірме: Тест нәтижесі күтілген мәнге сәйкес екенін тексереді.
    expect(price).toBe(875);
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  });

  // Түсіндірме: Нақты бір тест сценарийін анықтайды.
  test("never drops below liquidation floor", () => {
    // Түсіндірме: Айнымалыны жариялап, оған бастапқы мән береді.
    const price = calculateDecayedPrice({
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      currentPrice: 520,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      liquidationPrice: 500,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      lastSaleDate: new Date("2026-03-01T00:00:00Z"),
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      now: new Date("2026-05-01T00:00:00Z"),
      // Түсіндірме: Тесттерден кейін орындалатын тазалау қадамын анықтайды.
      afterDays: 30,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      decayPercent: 20,
      // Түсіндірме: Объект, схема немесе конфигурация өрісін анықтайды.
      decayIntervalDays: 3
    // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
    });

    // Түсіндірме: Тест нәтижесі күтілген мәнге сәйкес екенін тексереді.
    expect(price).toBe(500);
  // Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
  });
// Түсіндірме: Ашылған код блогын немесе өрнекті жабады.
});
